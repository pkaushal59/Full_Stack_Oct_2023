/**
 *  What is fetch API?
 * 
 *     - Fetch API is provided by the browser not via the javascript itself.
 *     - Fetch API helps JS developer to the API calls for submitting or getting data from the server.
 *     - we use fetch() function -> returns a promise to resolve or reject the response of the request.
 * 
 *     - syntax:
 *         fetch(url, [options]);
 * 
 *        url - the end points (REST API)
 *        options (optional) - an object contains additional information for the request like headers(JWT), request method(PUT/POST/DELETE/GET), body
 * 
 *     - how to consume the fetch API?
 * 
 *         1. either use then(), catch()& finally().
 *         2. you async/await
 */

